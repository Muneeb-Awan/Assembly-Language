INCLUDE MUNEEB.INC
.MODEL SMALL
.STACK 100H

.DATA

  NUM DB ?
  MSG1 DB 'ENTER A NUMBER: $'
  MSG2 DB 'NOT A PRIME NUMBER $'
  MSG3 DB 'IT IS A PRIME NUMBER $'

.CODE
MAIN PROC

  MOV AX,@DATA
  MOV DS,AX
    NEW_LINE
    PUT_S MSG1

    GET_D NUM

  CMP AL,1
  JLE NOT_P
  MOV AH,00
  CMP AL,3
  JLE PRIME
  MOV AH,00

  MOV CL,2
  DIV CL
  MOV CL,AL
 
DO:

  MOV AH,00
  MOV AL,NUM
  DIV CL
  CMP AH,00
  JZ NOT_P
  DEC CL
  CMP CL,1
  JNE DO
  JMP PRIME

 NOT_P:
    NEW_LINE
    PUT_S MSG2
    JMP END

 PRIME:
    NEW_LINE
    PUT_S MSG3 
END: 


INCLUDE MUNEEB.INC
.MODEL SMALL
.STACK 100H
.DATA
STR1 db 20 dup('$')
STR2 db 20 dup('$')
COUNT dw 0
.CODE
MOV DX,@data
MOV DS,DX
MOV ES,DX

MOV CX,20
MOV SI,0

INPUT:
GET_C STR1[SI] 
CMP AL,13
JE NEXT
INC SI 
INC COUNT
LOOP INPUT

NEXT:
MOV SI,COUNT
PUSH COUNT
INC COUNT
MOV CX,COUNT
POP COUNT
MOV DI,0
REVERSE:

MOV BH,STR1[SI]
MOV STR2[DI],BH

INC DI
DEC SI

LOOP REVERSE

MOV CX,COUNT
MOV SI,0
IS_PALINDROME:
MOV BH,STR1[SI]
CMP STR2[SI+1],BH
JNE NO 
INC SI
LOOP IS_PALINDROME

NEW_LINE
PRINT "YES ITS A PALINDROME STRING"
JMP END

NO:

NEW_LINE
PRINT "NOT PALINDROME STRING"  

END:







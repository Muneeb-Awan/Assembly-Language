INCLUDE MUNEEB.INC
.MODEL SMALL
.STACK 100H
.DATA
GET DB "ENTER A 8-BIT BINARY STREAM: $"
HEX DB "HEX VALUE OF GIVEN NUMBER IS: $"
DIGIT1 DB 0
DIGIT2 DB 0
.CODE 
MOV DX,@DATA
MOV DS,DX
;;DIGIT 1
PUT_S GET
GET_D BH
SHL BH,3
ADD DIGIT1,BH
GET_D BH
SHL BH,2
ADD DIGIT1,BH
GET_D BH
SHL BH,1
ADD DIGIT1,BH
GET_D BH
ADD DIGIT1,BH

;;DIGIT 2
GET_D BH
SHL BH,3
ADD DIGIT2,BH
GET_D BH
SHL BH,2
ADD DIGIT2,BH
GET_D BH
SHL BH,1
ADD DIGIT2,BH
GET_D BH
ADD DIGIT2,BH 

NEW_LINE
PUT_S HEX

CMP DIGIT1,9
JG DO_HEX1
PUT_D DIGIT1
CMP DIGIT2,9
JG DO_HEX2 
PUT_D DIGIT2
JMP END  

DO_HEX1:
CMP DIGIT1,10
JE P10
CMP DIGIT1,11
JE P11
CMP DIGIT1,12
JE P12
CMP DIGIT1,13
JE P13
CMP DIGIT1,14
JE P14
CMP DIGIT1,15
JE P15
JMP END

DO_HEX2:
CMP DIGIT2,10
JE P210
CMP DIGIT2,11
JE P211
CMP DIGIT2,12
JE P212
CMP DIGIT2,13
JE P213
CMP DIGIT2,14
JE P214
CMP DIGIT2,15
JE P215
JMP END

P10:
PUT_C "A"
JMP DO_HEX2
P11:
PUT_C "B"
JMP DO_HEX2
P12:
PUT_C "C"
JMP DO_HEX2
P13:
PUT_C "D"
JMP DO_HEX2
P14:
PUT_C "E"
JMP DO_HEX2
P15:
PUT_C "F"
JMP DO_HEX2

P210:
PUT_C "A"
JMP END
P211:
PUT_C "B"
JMP END
P212:
PUT_C "C"
JMP END
P213:
PUT_C "D"
JMP END
P214:
PUT_C "E"
JMP END
P215:
PUT_C "F"
JMP END

END:
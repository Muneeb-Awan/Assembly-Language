INCLUDE MUNEEB.INC
.MODEL SMALL
.STACK 100H
.DATA

NUM DB 0
RD_3 DB ? ;; RESULT CAN BE 3 DIGITS SO RESULUT DIGIT _3
RD_2 DB ? ;; RESULT DIGIT_2
RD_1 DB ? ;; RESULT DIGIT_1

.CODE

START: PRINT "ENTER A 8-BIT BINARY NUMBER."  
NEW_LINE

MOV DX,@DATA
MOV DS,DX

GET_D BL
SHL BL,7
ADD NUM,BL

GET_D BL
SHL BL,6
ADD NUM,BL

GET_D BL
SHL BL,5
ADD NUM,BL

GET_D BL
SHL BL,4
ADD NUM,BL

GET_D BL
SHL BL,3
ADD NUM,BL

GET_D BL
SHL BL,2
ADD NUM,BL

GET_D BL
SHL BL,1
ADD NUM,BL 

GET_D BL
SHL BL,0
ADD NUM,BL 

;;; PRINTING THE NUMBER

MOV AL,NUM
AAM
MOV RD_3,AL
MOV AL,AH
XOR AH,AH
AAM
MOV RD_2,AL
MOV RD_1,AH

NEW_LINE
PRINT "NUMBER IS : "


PUT_D RD_1
PUT_D RD_2
PUT_D RD_3 

NEW_LINE 

MOV NUM,0 ;;RESET NUMBER

JMP START
